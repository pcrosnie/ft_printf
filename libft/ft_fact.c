#include "libft.h"

long	ft_fact(int n)
{
	long nb;

	nb = (int)n;
	if (n == 0)
		return (1);
	if (n < 0)
		return (0);
	n--;
	while (n > 0)
	{
		nb *= n;
		n--;
	}
	return (nb);
}
