#include "libft.h"

void	ft_putendl(char const *s)
{
	int		i;
	char	str[ft_strlen(s) + 1];

	i = 0;
	while (s[i])
	{
		str[i] = s[i];
		i++;
	}
	str[i] = '\n';
	str[i + 1] = '\0';
	write(1, str, (ft_strlen(s) + 1));
}
